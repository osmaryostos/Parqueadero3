create table VEHICULO(
VEH_ID BIGINT AUTO_INCREMENT PRIMARY KEY,
PLACA VARCHAR(16) NOT NULL UNIQUE,
TIPO BIGINT NOT NULL ,
COLOR VARCHAR(16),
CILINDRAJE BIGINT NOT NULL,
H_LLEGADA BIGINT NOT NULL,
H_SALIDA BIGINT
);

create table PARQUEADERO(
PUESTO BIGINT AUTO_INCREMENT PRIMARY KEY,
PLACA VARCHAR(16)
);

ALTER TABLE PARQUEADERO ADD FOREIGN KEY (PLACA) REFERENCES VEHICULO(PLACA);
CREATE INDEX IDX_PLACA ON PARQUEADERO(PLACA);